 num1 = parseInt (prompt( 'inserisci primo numero' ));

 num2 = parseInt (prompt( 'Inserisci secondo numero' ));

let somma = num1 + num2;

let sottrazione = num1 - num2;

let moltiplicazione = num1 * num2;

let divisione = num1 / num2;

let potenza = num1 ** num2;

console.log( `Con i numeri da te scelti, i risultati delle varie operazioni sono somma ${somma} , sottrazione ${sottrazione} , moltiplicazione ${moltiplicazione} , divisione ${divisione} , elevazione a potenza ${potenza} `);